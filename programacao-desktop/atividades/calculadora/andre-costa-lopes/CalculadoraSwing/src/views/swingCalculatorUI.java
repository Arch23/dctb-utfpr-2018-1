    private void backspaceButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backspaceButtonActionPerformed
        // TODO add your handling code here:
        int length = panel.getText().length();
        int number = panel.getText().length() -1;
        String store;
        
        if(length >0){
            StringBuilder back = new StringBuilder(panel.getText());
            back.deleteCharAt(number);
            store = back.toString();
            panel.setText(store);
        }
      
    }//GEN-LAST:event_backspaceButtonActionPerformed

