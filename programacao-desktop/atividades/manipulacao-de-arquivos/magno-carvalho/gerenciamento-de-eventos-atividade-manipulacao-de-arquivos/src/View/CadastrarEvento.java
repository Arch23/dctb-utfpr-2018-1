    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        Evento e = new Evento();
        TextoEvento be = new TextoEvento();
        try {
            limparCampos();
            e = be.lerTexto();
            setValoresFront(e);
            
        } catch (IOException ex) {
            Logger.getLogger(CadastrarEvento.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        GSonEventos ev = new GSonEventos();
        String convertJSON = ev.convertToJSON(getValoresFront());
        ev.saveJSON("./JsonEvento.json", convertJSON);
        limparCampos();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        GSonEventos ev = new GSonEventos();
        Evento e =  new Evento();
        e = ev.converToPerson(ev.loadJSON("./JsonEvento.json"));
        limparCampos();
        setValoresFront(e);
        
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        CreateBinEvento cb = new CreateBinEvento();
        Evento ev = new Evento();
        ev = getValoresFront();
        cb.addRecords(ev);
        limparCampos();
    }//GEN-LAST:event_jButton3ActionPerformed

