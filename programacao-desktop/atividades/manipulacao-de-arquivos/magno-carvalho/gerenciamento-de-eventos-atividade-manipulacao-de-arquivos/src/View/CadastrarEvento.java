    public void limparCampos()
    {
        cevTEqui.setText("");
        cevTEv.setText("");
        cevTSala.setText("");
        cevTLoc.setText("");
    }
    public void setValoresFront(Evento e)
    {
        cevTEqui.setText(e.getEquipamento());
        cevTEv.setText(e.getEvento());
        cevTSala.setText(e.getSala());
        cevTLoc.setText(e.getLocal());
    }
    public Evento getValoresFront()
    {
        Evento e = new Evento();
        e.setEquipamento(cevTEqui.getText());
        e.setEvento(cevTEv.getText());
        e.setLocal(cevTLoc.getText());
        e.setSala(cevTSala.getText());
        return e;
        
    }

