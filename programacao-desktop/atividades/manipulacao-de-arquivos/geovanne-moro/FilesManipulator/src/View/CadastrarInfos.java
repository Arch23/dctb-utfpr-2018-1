public void limparCampos()
    {
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
    }
    public void setValoresFront(Info e)
    {
        jTextField1.setText(e.getName());
        jTextField2.setText(e.getAddress());
        jTextField3.setText(e.getAge());
        jTextField4.setText(e.getOccupation());
    }
    public Info getValoresFront()
    {
        Info e = new Info();
        e.setName(jTextField1.getText());
        e.setAddress(jTextField2.getText());
        e.setAge(jTextField3.getText());
        e.setOccupation(jTextField4.getText());
        return e;
        
    }
