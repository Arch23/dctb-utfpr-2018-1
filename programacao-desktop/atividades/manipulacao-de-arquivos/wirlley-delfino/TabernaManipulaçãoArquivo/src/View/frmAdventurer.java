    public Adventurer getValues(){
        Adventurer a = new Adventurer();
        a.setNome(txtName.getText());
        a.setGold(txtGold.getText());
        return a;
    }
    
    public void cleanFields(){
        txtGold.setText("");
        txtName.setText("");
    }
    
    public void setValues(Adventurer a){
        txtName.setText(a.getNome());
        txtGold.setText(a.getGold());
    }
