    private void btn_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvarActionPerformed
        Usuario user = new Usuario(this.txt_nome.getText(), this.txt_email.getText(), new String (this.txt_senha.getPassword()), this.txt_cartao.getText());
        switch(this.jcb_opcaosalvar.getSelectedIndex()){
            case 0:
                String[] dados = {this.txt_nome.getText(), this.txt_email.getText(), new String (this.txt_senha.getPassword()), this.txt_cartao.getText()};
                arquivo.salvarArquivoTXT(dados);
                break;
            case 1:
                arquivo.salvarArquivoJSON(user);
                break;
            case 2:
                arquivo.salvarArquivoBinario(user);
                break;
        }
        
        limparCampos();
    }//GEN-LAST:event_btn_salvarActionPerformed

