    private void jButtonEqualsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEqualsActionPerformed

        Float number1, number2, result;

        number1 = calculator.getNumber1();

        number2 = Float.parseFloat(jTextFieldScreen.getText());

        String signal = calculator.getSignal();

        calculator.setNumber2(number2);

        result = calculus.calculate(signal, number1, number2);

        jTextFieldScreen.setText(result.toString());

    }//GEN-LAST:event_jButtonEqualsActionPerformed

